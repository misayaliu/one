package jieyi.app.form.chart;


public class t_chart_column {

	private String chart_column_id;
	
	/**
	 * 所属区域图表颜色
	 */
	private String chart_color;
	
	/**
	 * 指定显示列名
	 */
	private String chart_data;
	
	/**
	 * 外键
	 */
	private String chart_id;
	
	/**
	 * 图表名称
	 */
	private String chart_textdesc;

	public String getChart_column_id() {
		return chart_column_id;
	}

	public void setChart_column_id(String chart_column_id) {
		this.chart_column_id = chart_column_id;
	}


	public String getChart_color() {
		return chart_color;
	}

	public void setChart_color(String chart_color) {
		this.chart_color = chart_color;
	}

	public String getChart_data() {
		return chart_data;
	}

	public void setChart_data(String chart_data) {
		this.chart_data = chart_data;
	}

	public String getChart_id() {
		return chart_id;
	}

	public void setChart_id(String chart_id) {
		this.chart_id = chart_id;
	}

	public String getChart_textdesc() {
		return chart_textdesc;
	}

	public void setChart_textdesc(String chart_textdesc) {
		this.chart_textdesc = chart_textdesc;
	}


	
	

}
